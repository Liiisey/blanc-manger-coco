                <h2>Le principe du jeu</h2>
                <p>Le principe de Blanc-manger Coco est très simple:</p>
                <ul>
                    <li>Chaque joueur reçoit 11 cartes blanches pour commencer.</li>
                    <li>Un premier joueur (le « Question Master ») lit une carte noire (texte à trou) prise dans la pioche.</li>
                    <li>Les autres joueurs choisissent une carte parmi leur jeu et la posent face retournée sur la table.</li>
                    <li>Le Question Master mélange les cartes blanches puis relit la carte noire avec chaque carte blanche.<br><strong>Il choisit la meilleure selon ses propres critères.</strong></li>
                </ul>
                <h2>Comment gagner ?</h2>
                <p>Après chaque manche, tous les joueurs s’assurent qu’ils ont bien 11 cartes blanches dans leur jeu, sinon ils repiochent des cartes.</p>
                <p>Le vainqueur de la manche précédente garde la carte noire qu’il a gagné et en pioche une nouvelle pour la lire: il devient le Question Master.</p>
                <p><strong>Le premier joueur qui a 5 (ou 10 ou 20 pour les plus fous) cartes noires a gagné la partie.</strong></p>
                <br>
